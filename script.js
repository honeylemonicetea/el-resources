let cards = [];
let cardsContainer = document.querySelector(".cards-container");
words.map((word) => {
  let card = document.createElement("div");
  card.classList.add("card");
  let cardBack = document.createElement("div");
  cardBack.classList.add("card-back");
  cardBack.innerHTML = `
        <h1 class="card-word">${word.word}</h1>
        <img src="${word.img}" alt="">
        <h2 class="card-translation">${word.translation}</h2>`;
  let cardFront = document.createElement("div");
  cardFront.classList.add("card-front");
  cardFront.innerHTML = `<h1 class="card-word">${word.word}</h1>`;
  card.appendChild(cardBack);
  card.appendChild(cardFront);
  cardsContainer.appendChild(card);
  card.addEventListener("click", toggleOpenCard);
  cards.push(card);
});

function toggleOpenCard(event) {
   
    let targetCard
    if(event.target.parentElement.classList.contains("card")){
        targetCard = event.target.parentElement
    }
    if (event.target.parentElement.classList.contains("card-back") || event.target.parentElement.classList.contains("card-front")){
        targetCard = event.target.parentElement.parentElement
    }
    if (targetCard) {
        targetCard.classList.toggle("card_open");
         
  }
}
